package com.footballmanagergamesimulator.dynamics;

/**
 * The manager's chosen tone for a team meeting or player conversation.
 * The reaction depends on this tone combined with personality, hierarchy,
 * playing time, morale, manager reputation, relationship and promises.
 */
public enum DynamicsTone {
    CALM,
    PASSIONATE,
    ASSERTIVE,
    DEMANDING,
    RELAXED,
    SYMPATHETIC
}
