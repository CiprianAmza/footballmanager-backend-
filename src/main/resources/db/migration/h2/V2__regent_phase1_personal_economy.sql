INSERT INTO personal_account
    (profile_id, owner_user_id, owner_human_id, cash_balance,
     lifetime_career_earnings, realized_investment_gain, version)
SELECT p.id, p.user_id, p.human_id,
       CASE WHEN p.career_type = 'CHAIRMAN' AND p.human_id IS NULL THEN 10000000
            ELSE GREATEST(COALESCE(h.wealth, 0), 0) END,
       GREATEST(COALESCE(h.career_earnings, 0), 0), 0, 0
FROM person_profile p
LEFT JOIN human h ON h.id = p.human_id
WHERE p.career_type IN ('PLAYER', 'MANAGER', 'CHAIRMAN')
  AND NOT EXISTS (SELECT 1 FROM personal_account a WHERE a.profile_id = p.id);

INSERT INTO personal_ledger_entry
    (account_id, profile_id, season_number, game_day, entry_type, signed_amount,
     career_earnings_delta, balance_after, correlation_id, idempotency_key,
     description, created_at)
SELECT a.id, a.profile_id, 0, 0, 'MIGRATION_OPENING', a.cash_balance,
       a.lifetime_career_earnings, a.cash_balance,
       CONCAT('PROFILE-MIGRATION:', a.profile_id), CONCAT('PROFILE-MIGRATION:', a.profile_id),
       'Migrated opening balance', 0
FROM personal_account a
WHERE (a.cash_balance <> 0 OR a.lifetime_career_earnings <> 0)
  AND NOT EXISTS (SELECT 1 FROM personal_ledger_entry l WHERE l.account_id = a.id);

INSERT INTO owned_asset
    (account_id, profile_id, catalog_item_id, purchase_price, current_value,
     purchase_season, purchase_day, status, purchase_idempotency_key, version)
SELECT pa.id, pa.profile_id, catalog.id,
       GREATEST(legacy.asset_value, 0), GREATEST(legacy.asset_value, 0),
       0, 0, 'OWNED', CONCAT('LEGACY-ASSET:', legacy.id), 0
FROM asset legacy
JOIN personal_account pa ON pa.owner_human_id = legacy.owner_human_id
JOIN asset_catalog_item catalog
  ON catalog.code = CASE WHEN legacy.type = 'CAR' THEN 'CAR_SPORT' ELSE 'VILLA_CLASSIC' END
WHERE legacy.type IN ('HOUSE', 'CAR')
  AND NOT EXISTS (
      SELECT 1 FROM owned_asset owned
      WHERE owned.account_id = pa.id
        AND owned.purchase_idempotency_key = CONCAT('LEGACY-ASSET:', legacy.id));
