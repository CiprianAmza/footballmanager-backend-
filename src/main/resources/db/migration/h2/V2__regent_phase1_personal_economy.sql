ALTER TABLE human ADD COLUMN IF NOT EXISTS wealth BIGINT DEFAULT 0 NOT NULL;
ALTER TABLE human ADD COLUMN IF NOT EXISTS career_earnings BIGINT DEFAULT 0 NOT NULL;

CREATE TABLE IF NOT EXISTS asset (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    owner_human_id BIGINT NOT NULL,
    type VARCHAR(10),
    name VARCHAR(255),
    asset_value BIGINT DEFAULT 0 NOT NULL,
    club_team_id BIGINT
);

DELETE FROM person_profile
WHERE user_id IS NULL
  AND human_id IN (SELECT id FROM human WHERE type_id NOT IN (1, 4));

CREATE TABLE IF NOT EXISTS personal_account (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    profile_id BIGINT NOT NULL,
    owner_user_id INTEGER,
    owner_human_id BIGINT,
    cash_balance BIGINT DEFAULT 0 NOT NULL,
    lifetime_career_earnings BIGINT DEFAULT 0 NOT NULL,
    realized_investment_gain BIGINT DEFAULT 0 NOT NULL,
    version BIGINT DEFAULT 0 NOT NULL,
    CONSTRAINT uk_personal_account_profile UNIQUE (profile_id),
    CONSTRAINT uk_personal_account_user UNIQUE (owner_user_id),
    CONSTRAINT uk_personal_account_human UNIQUE (owner_human_id),
    CONSTRAINT ck_personal_account_cash_nonnegative CHECK (cash_balance >= 0),
    CONSTRAINT ck_personal_account_earnings_nonnegative CHECK (lifetime_career_earnings >= 0)
);

CREATE TABLE IF NOT EXISTS personal_ledger_entry (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    account_id BIGINT NOT NULL,
    profile_id BIGINT NOT NULL,
    season_number INTEGER DEFAULT 0 NOT NULL,
    game_day INTEGER DEFAULT 0 NOT NULL,
    entry_type VARCHAR(40) NOT NULL,
    signed_amount BIGINT NOT NULL,
    career_earnings_delta BIGINT DEFAULT 0 NOT NULL,
    balance_after BIGINT NOT NULL,
    correlation_id VARCHAR(120) NOT NULL,
    idempotency_key VARCHAR(160) NOT NULL,
    counterpart_team_id BIGINT,
    counterpart_asset_id BIGINT,
    description VARCHAR(300) NOT NULL,
    created_at BIGINT NOT NULL,
    CONSTRAINT uk_personal_ledger_idempotency UNIQUE (account_id, idempotency_key),
    CONSTRAINT ck_personal_ledger_balance_nonnegative CHECK (balance_after >= 0)
);

CREATE INDEX IF NOT EXISTS idx_personal_ledger_account_id
    ON personal_ledger_entry(account_id, id);
CREATE INDEX IF NOT EXISTS idx_personal_ledger_correlation
    ON personal_ledger_entry(correlation_id);

CREATE TABLE IF NOT EXISTS asset_catalog_item (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(60) NOT NULL,
    asset_type VARCHAR(20) NOT NULL,
    apartment_rooms INTEGER,
    name VARCHAR(120) NOT NULL,
    icon_key VARCHAR(80) NOT NULL,
    purchase_price BIGINT NOT NULL,
    resale_haircut_bps INTEGER DEFAULT 1000 NOT NULL,
    active BOOLEAN DEFAULT TRUE NOT NULL,
    version BIGINT DEFAULT 0 NOT NULL,
    CONSTRAINT uk_asset_catalog_code UNIQUE (code),
    CONSTRAINT ck_asset_catalog_price_positive CHECK (purchase_price > 0),
    CONSTRAINT ck_asset_catalog_haircut CHECK (resale_haircut_bps BETWEEN 0 AND 10000),
    CONSTRAINT ck_asset_catalog_apartment_rooms CHECK (
        (asset_type = 'APARTMENT' AND apartment_rooms BETWEEN 1 AND 4)
        OR (asset_type <> 'APARTMENT' AND apartment_rooms IS NULL))
);

CREATE TABLE IF NOT EXISTS owned_asset (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    account_id BIGINT NOT NULL,
    profile_id BIGINT NOT NULL,
    catalog_item_id BIGINT NOT NULL,
    purchase_price BIGINT NOT NULL,
    current_value BIGINT NOT NULL,
    purchase_season INTEGER DEFAULT 0 NOT NULL,
    purchase_day INTEGER DEFAULT 0 NOT NULL,
    status VARCHAR(20) NOT NULL,
    purchase_idempotency_key VARCHAR(160) NOT NULL,
    sale_idempotency_key VARCHAR(160),
    sale_price BIGINT,
    sale_season INTEGER,
    sale_day INTEGER,
    version BIGINT DEFAULT 0 NOT NULL,
    CONSTRAINT uk_owned_asset_purchase_retry UNIQUE (account_id, purchase_idempotency_key),
    CONSTRAINT uk_owned_asset_sale_retry UNIQUE (account_id, sale_idempotency_key),
    CONSTRAINT ck_owned_asset_values_nonnegative CHECK (
        purchase_price >= 0 AND current_value >= 0 AND (sale_price IS NULL OR sale_price >= 0))
);

CREATE INDEX IF NOT EXISTS idx_owned_asset_account_status ON owned_asset(account_id, status);

